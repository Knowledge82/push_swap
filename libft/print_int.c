#include "libft.h"

void	print_int(int nb, int *len)
{
	if (nb == -2147483648)
	{
		print_str("-2147483648", len);
		return ;
	}
	if (nb < 0)
	{
		print_char('-', len);
		nb *= -1;
	}
	if (nb >= 10)
		print_int(nb / 10, len);
	print_char((nb % 10) + '0', len);
}
